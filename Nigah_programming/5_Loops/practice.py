#WAP to factorial of first n numbers(using for)
n = 5
fact = 1
i = 1 
while i <= n:
  fact *= i
  i += 1
print("total sum =" , fact)


#WAP to factorial of first n numbers(using for)
n = 5
fact = 1
for i in range(1,n+1):
  fact *= i
print("factorial =" , fact)